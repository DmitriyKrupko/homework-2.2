a = int(input("Введите число a: "))
b = int(input("Введите число b: "))
print("Сумма чисел: ", a + b, 
    "\n" "Разность чисел: ", a - b, 
    "\n" "Результат умножения: ", a * b, 
    "\n" "Частное от деления чисел: ", a // b, 
    "\n" "Остаток от деления: ", a % b, 
    "\n" "Число а в степени b", a ** b )